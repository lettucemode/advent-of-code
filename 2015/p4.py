import aoc
import hashlib

input = aoc.getInput(4)
i = 0
found = 0

while True:
    hash = hashlib.md5((input + str(i)).encode('utf-8'))
    digest = hash.hexdigest()

    if(digest.startswith('000000')):
        print(digest, i)
        found += 1

    if (found == 4):
        break

    i += 1

